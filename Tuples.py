'''
Tuples are ordered collections of heterogenous data that are unchangeable
Tuples are:
1. ORDERED - maintains index value of each item.
2. UNCHANGABLE - cannot add or delete items to typles after creation
3. HETEROGENOUS - sequence of data of different data types - integeres, floats, strings, lists, etc
4. CONTAINS DUPLICATES - can contain duplicates, items of the same value
'''

#create a tuple using()

#number tuple
number_tuple = (10, 20, 25.75)
print(number_tuple)
#Output (10, 20, 25.75)

#string tuple
string_tuple = ("Jessa", "Emma", "Kelly")
print(string_tuple)
#Output ("Jessa", "Emma", "Kelly")

#mixed type tuple
sample_tuple = ("Jessa", 30, 45.75, [25, 78])
print(sample_tuple)
#Output ('Jessa', 30, 45.75, [25, 78])

